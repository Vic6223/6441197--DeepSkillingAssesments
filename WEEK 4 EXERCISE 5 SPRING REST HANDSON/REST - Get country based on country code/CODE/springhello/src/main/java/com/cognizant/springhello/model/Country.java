package com.cognizant.springhello.model;

public class Country {

    private String code;
    private String name;

    // Default constructor
    public Country() {}

    // Getter for 'code'
    public String getCode() {
        return code;
    }

    // Setter for 'code'
    public void setCode(String code) {
        this.code = code;
    }

    // Getter for 'name'
    public String getName() {
        return name;
    }

    // Setter for 'name'
    public void setName(String name) {
        this.name = name;
    }
}
